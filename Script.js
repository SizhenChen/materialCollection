let fadeInElements = document.querySelectorAll('.fade-in'); //图片呈表格出现时fade in

// Add event listener for when the user scrolls
window.addEventListener('scroll', function () {
    // Check each fade-in element to see if it's in view
    for (let m = 0; m < fadeInElements.length; m++) {
        let fadeInElement = fadeInElements[m];
        let fadeInPosition = fadeInElement.getBoundingClientRect().top + window.pageYOffset;
        let windowHeight = window.innerHeight;
        if (fadeInPosition < (windowHeight + window.pageYOffset)) {
            // Add the 'is-visible' class to fade in the element
            fadeInElement.classList.add('is-visible');
        }
    }
});

